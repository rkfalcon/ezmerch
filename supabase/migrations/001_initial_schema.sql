-- EZMerch Initial Schema
-- Multi-tenant POD e-commerce platform

-- ============================================================
-- ENUMS
-- ============================================================

CREATE TYPE public.app_role AS ENUM ('admin', 'store_owner', 'customer');

CREATE TYPE public.shipping_policy AS ENUM ('passthrough', 'free', 'flat_discount');

-- ============================================================
-- TABLES
-- ============================================================

-- Stores (core tenant table)
CREATE TABLE public.stores (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  slug text UNIQUE NOT NULL,
  name text NOT NULL,
  owner_id uuid REFERENCES auth.users(id),
  stripe_account_id text,
  claim_token text UNIQUE,
  claim_token_expires_at timestamptz,
  claimed boolean DEFAULT false NOT NULL,
  brand_colors jsonb DEFAULT '{"primary": "#000000", "accent": "#3b82f6"}'::jsonb NOT NULL,
  logo_url text,
  shipping_policy public.shipping_policy DEFAULT 'passthrough' NOT NULL,
  shipping_discount_cents integer,
  created_at timestamptz DEFAULT now() NOT NULL,
  updated_at timestamptz DEFAULT now() NOT NULL
);

-- Products (belong to a store)
CREATE TABLE public.products (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  store_id uuid REFERENCES public.stores(id) ON DELETE CASCADE NOT NULL,
  printful_sync_product_id bigint,
  title text NOT NULL,
  description text,
  variants jsonb DEFAULT '[]'::jsonb NOT NULL,
  published boolean DEFAULT false NOT NULL,
  thumbnail_url text,
  created_at timestamptz DEFAULT now() NOT NULL,
  updated_at timestamptz DEFAULT now() NOT NULL
);

-- Orders
CREATE TABLE public.orders (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  store_id uuid REFERENCES public.stores(id) NOT NULL,
  customer_id uuid REFERENCES auth.users(id),
  customer_email text NOT NULL,
  stripe_payment_intent_id text UNIQUE,
  printful_order_id bigint,
  status text DEFAULT 'pending' NOT NULL
    CHECK (status IN ('pending', 'paid', 'fulfilling', 'fulfillment_failed', 'shipped', 'delivered', 'failed', 'refunded')),
  printful_error text,
  retry_count integer DEFAULT 0 NOT NULL,
  subtotal_cents integer NOT NULL,
  shipping_cents integer DEFAULT 0 NOT NULL,
  tax_cents integer DEFAULT 0 NOT NULL,
  total_cents integer NOT NULL,
  platform_fee_cents integer DEFAULT 0 NOT NULL,
  shipping_address jsonb NOT NULL,
  tracking_number text,
  tracking_url text,
  created_at timestamptz DEFAULT now() NOT NULL,
  updated_at timestamptz DEFAULT now() NOT NULL
);

-- Order items
CREATE TABLE public.order_items (
  id uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  order_id uuid REFERENCES public.orders(id) ON DELETE CASCADE NOT NULL,
  product_id uuid REFERENCES public.products(id) NOT NULL,
  variant_key text NOT NULL,
  quantity integer NOT NULL CHECK (quantity > 0),
  price_cents integer NOT NULL CHECK (price_cents > 0)
);

-- Profiles
CREATE TABLE public.profiles (
  id uuid REFERENCES auth.users(id) ON DELETE CASCADE PRIMARY KEY,
  display_name text,
  avatar_url text,
  created_at timestamptz DEFAULT now() NOT NULL,
  updated_at timestamptz DEFAULT now() NOT NULL
);

-- User roles
CREATE TABLE public.user_roles (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id uuid REFERENCES auth.users(id) ON DELETE CASCADE NOT NULL,
  role public.app_role NOT NULL,
  store_id uuid REFERENCES public.stores(id) ON DELETE CASCADE,
  UNIQUE (user_id, role, store_id)
);

-- ============================================================
-- INDEXES
-- ============================================================

CREATE INDEX idx_stores_owner_id ON public.stores(owner_id);
CREATE INDEX idx_stores_slug ON public.stores(slug);
CREATE INDEX idx_stores_claim_token ON public.stores(claim_token) WHERE claim_token IS NOT NULL;
CREATE INDEX idx_products_store_id ON public.products(store_id);
CREATE INDEX idx_products_published ON public.products(store_id, published) WHERE published = true;
CREATE INDEX idx_orders_store_id ON public.orders(store_id);
CREATE INDEX idx_orders_customer_id ON public.orders(customer_id) WHERE customer_id IS NOT NULL;
CREATE INDEX idx_orders_status ON public.orders(status);
CREATE INDEX idx_orders_stripe_pi ON public.orders(stripe_payment_intent_id) WHERE stripe_payment_intent_id IS NOT NULL;
CREATE INDEX idx_order_items_order_id ON public.order_items(order_id);
CREATE INDEX idx_user_roles_user_id ON public.user_roles(user_id);

